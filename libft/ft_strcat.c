#include "includes/libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int i;
	int size;

	i = 0;
	size = ft_strlen(dest);
	while (src[i] != '\0')
	{
		dest[size + i] = src[i];
		i++;
	}
	dest[size + i] = '\0';
	return (dest);
}
